print("========================")
print("Operadores Relacionales")
print("========================\n")
print("\n")

numero_uno = float(input("Teclea un numero: "))
numero_dos = float(input("Teclea otro numero: "))

print("\n Los numero a comparar son: ", numero_uno , " y ", numero_dos, "\n")

if numero_uno == numero_dos:
    print("Es igual...")
if numero_uno != numero_dos:
    print("Es diferente...")
if numero_uno > numero_dos:
    print("Es mayor...")
if numero_uno >= numero_dos:
    print("Es mayor o igual...")
if numero_uno < numero_dos:
    print("Es menor...")
if numero_uno <= numero_dos:
    print("Es menor o igual...")
